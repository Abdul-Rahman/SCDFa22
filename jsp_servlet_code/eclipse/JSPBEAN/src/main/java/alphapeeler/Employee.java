package alphapeeler;

public class Employee {
	private int id;
	private String name;
	public Employee (){
		
	}
	public void setId(int i) {
		this.id = i;
	}
	public int getId() {
		return this.id;
	}

	public void setName(String n) {
		this.name = n;
	}
	
	public String getName() {
		return this.name;
	}
	
}
